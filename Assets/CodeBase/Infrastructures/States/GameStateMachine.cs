using System;
using System.Collections.Generic;
using CodeBase.Infrastructures.Factory;
using CodeBase.Infrastructures.PlayableObjectsStorage;
using CodeBase.Infrastructures.Services;
using CodeBase.Services.InputService;
using CodeBase.Services.PreseneterStock;

namespace CodeBase.Infrastructures.States
{
    public class GameStateMachine
    {
        private readonly Dictionary<Type, IExitableState> _states;
        private IExitableState _activeState;

        public GameStateMachine(SceneLoader sceneLoader, AllServices services)
        {
            _states = new Dictionary<Type, IExitableState>
            {
                [typeof(BootstrapState)] = new BootstrapState(this, sceneLoader, services),
                [typeof(LoadResourceState)] = new LoadResourceState(this, sceneLoader, services.Single<IGameFactory>(), 
                    services.Single<IInputService>(), services.Single<IUpdateKeeper>(), services.Single<IPresenterStockService>())
            };
        }

        public void Enter<TState>() where TState : class, IState
        {
            IState state = ChangeState<TState>();
            state.Enter();
        }
        
        public void Enter<TState, TPayload>(TPayload payload) where TState : class, IPayloadedState<TPayload>
        {
            TState state = ChangeState<TState>();
            state.Enter(payload);
        }
        
        

        private TState ChangeState<TState>() where TState : class, IExitableState
        {
            _activeState?.Exit();

            TState state = GetState<TState>();
            _activeState = state;

            return state;
        }

        private TState GetState<TState>() where TState : class, IExitableState => 
            _states[typeof(TState)] as TState;
    }
}